package Oct.ex_21102024_ForLoop;

public class Lab099 {
    public static void main(String[] args) {
        // Break and For Loop
        for (int i = 0; i < 50; i++) {
            if(i == 5){
                break; // come out of loop - in this case FOR Loop
            }
            System.out.println(i);
        }
    }
}
