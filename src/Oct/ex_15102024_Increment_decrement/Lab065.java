package Oct.ex_15102024_Increment_decrement;

public class Lab065 {
    public static void main(String[] args) {
        int a = 10;
        int result = ++a; // a = 11, result = 11
        System.out.println(a); // 11
        System.out.println(result); // 11

        // Method
        // Line No. | Exp(result)  |  a
        //   5    | NA | 10
        //   6    | 11 | 11

    }
}
