package Oct.ex_11102024_Operators;
public class Lab046 {
    public static void main(String[] args) {
        // == -> compare between two values
        System.out.println(10 == 10); // true - because 10 is equal to 10
        System.out.println(10 == 20); // false - because 10 is not equal to 20
        System.out.println(10 < 10); // false - because 10 is not less than 10
        System.out.println(10 <= 10); // 10 < 10 or 10 = 10
        System.out.println(10 >= 10); //  10> 10 or 10 = 10
    }
}
