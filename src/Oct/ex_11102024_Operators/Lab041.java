package Oct.ex_11102024_Operators;

public class Lab041 {
    public static void main(String[] args) {
        // Unary Operator - is an operator that takes only one operand - example: +, - , ++, --, !
        // * and / are binary operators - takes two operands - example: 10 * 20 -> 10 and 20 are two operands
        int a = 65; // + ->  unary operator
        int b = -1; // - ->  unary operator
        System.out.println(a); // 65
        System.out.println(b); // -1
        b = b+1;  // -1 + 1 = 0
        System.out.println(b);  // 0
    }
}
