package Oct.ex_23102024_Functions;

public class Lab115 {
    // Method OverLoad - OOPs
    public static void main(String[] args) {

    }
    //  1.Without Parameters and Without Return Type
    // 2.Without Parameters but With Return Type
    // 3.With Parameters and Without Return Type
    // 4. With Parameters and With Return Type

    //  1. Without Argument / Parameters and Without Return Type.
    // public static void main() {} // Main Method Without Argument / Parameters and Without Return Type is Not Possible
    // This concept is called Method Overloading - Main Method Overloading //


    //   2. Without Parameters but With Return Type
    public static int main() {
        return 18;
    }


    //3 With Parameters and Without Return Type
    public static void main(int a) {
    }

    public static void main(String args) {
    }

    // 4. With Parameters and With Return Type
    public static String main(String a, String b) {
        return a + b;
    }
}
